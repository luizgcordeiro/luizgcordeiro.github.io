<p><span class='envidentifier'>Theorem 4.4.32</span>
Let $\theta=\left(\theta_s:X_{s^*}\to X_s\right)_{s\in S}$ be a topological partial action of an inverse semigroup $S$ on a zero-dimensional, locally compact Hausdorff space $X$, and $\alpha=\left(\alpha_s:D_{s^*}\to D_s\right)_{s\in S}$ the partial action induced by $\theta$ (Definition <a class="makeref" data-target="refdefinitiondualpartialaction" href="#definitiondualpartialaction">4.4.29</a>).
</p>
<p>
If the groupoid of germs $S \ltimes_\theta X$ is Hausdorff then the Steinberg $A_R(S \ltimes_\theta X)$ is isomorphic, as an $R$-algebra, to the crossed product $A_R(X) \rtimes_\alpha S$.
</p>
